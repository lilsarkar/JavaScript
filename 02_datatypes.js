"use strict"; // Treat JS as a newer version

// console.log(3 + 3); // Use console.log instead of alert in Node.js

console.log(33 + 3); // Output: 36

const name = "saad"; // String
const age = 23; // Number
const isLoggedIn = false; // Boolean

// Data Types in JavaScript
// number => 2 to the power of 53
// bigint => For very large numbers
// string => "" or ''
// boolean => true/false
// null => Standalone value or type
// undefined => Variable declared but not assigned a value
// symbol => Unique identifier
// object => Collection of key-value pairs

console.log(typeof age); // Output: number
console.log(typeof name); // Output: string
console.log(typeof isLoggedIn); // Output: boolean
console.log(typeof null); // Output: object (this is a known quirk in JavaScript)
console.log(typeof undefined); // Output: undefined